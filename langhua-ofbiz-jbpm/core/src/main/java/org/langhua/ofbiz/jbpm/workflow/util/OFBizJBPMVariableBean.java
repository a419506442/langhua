package org.langhua.ofbiz.jbpm.workflow.util;

/**
 * Variable Bean Implementation.
 * 
 * @author David Loiseau
 * @author Shi Yusen
 * @author Shang Zhengwu,shangzw@langhua.cn
 */

public class OFBizJBPMVariableBean extends Object {

	String name;
	Object value;

	public OFBizJBPMVariableBean(String name, Object value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}

	/**
	 * @see java.lang.Object#clone()
	 */
	public Object clone() {

		// create a copy of the jbpm variable bean
		OFBizJBPMVariableBean result = new OFBizJBPMVariableBean(name, value);
		return result;
	}

}
